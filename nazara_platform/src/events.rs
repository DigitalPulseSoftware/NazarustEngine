use winit::dpi::LogicalPosition;

#[derive(Clone, Copy, Debug, PartialEq)]
pub enum State {
    Pressed,
    Released,
}
#[derive(Clone, Copy, Debug, PartialEq)]
pub enum MouseButton {
    Left,
    Right,
    Middle,
    Other(u8),
}
#[derive(Clone, Copy, Debug, PartialEq)]
pub enum WindowEvent {
    Resized,
    Moved,
    CloseRequested,
}
#[derive(Clone, Copy, Debug, PartialEq)]
pub enum MouseEvent {
    Moved(LogicalPosition),
    Button {
        state: State,
        mouse_button: MouseButton,
    },
    Scroll,
}
#[derive(Clone, Copy, Debug, PartialEq)]
pub enum KeyState {
    Pressed(KeyCode),
    Released(KeyCode),
}
